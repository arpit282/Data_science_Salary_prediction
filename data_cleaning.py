import pandas as pd

df = pd.read_csv('glassdoor_jobs.csv')
df.head(30)

df = df[df['Salary Estimate'] != '-1']

df.head(20)

salary_RK = df['Salary Estimate'].apply(lambda x:x.replace('â‚¹',"").replace('K',''))
salary_RK.head(30)

df['Monthly'] = df['Salary Estimate'].apply(lambda x: 1 if '/mo' in x.lower() else 0)

df.head(10)

salary_month = salary_RK.apply(lambda x: x.replace('/mo','').replace(',',''))
salary_month.head(11)

df['salary'] = salary_month.apply(lambda x: float(x.split('-')[0]))
# df['max_salary'] = salary_month.apply(lambda x: float(x.split('-')[1]))

df.Location.value_counts()

df['age'] = df.Founded.apply(lambda x: x if x <1 else 2020 - x)

#parsing of job description (python, etc.)

#python
df['python_yn'] = df['Job Description'].apply(lambda x: 1 if 'python' in x.lower() else 0)
print(df['python_yn'].value_counts())
#r studio 
df['R_yn'] = df['Job Description'].apply(lambda x: 1 if 'r studio' in x.lower() or 'r-studio' in x.lower() else 0)
print(df.R_yn.value_counts())

#spark 
df['spark'] = df['Job Description'].apply(lambda x: 1 if 'spark' in x.lower() else 0)
print(df.spark.value_counts())

#aws 
df['aws'] = df['Job Description'].apply(lambda x: 1 if 'aws' in x.lower() else 0)
print(df.aws.value_counts())

#excel
df['excel'] = df['Job Description'].apply(lambda x: 1 if 'excel' in x.lower() else 0)
print(df.excel.value_counts())

df.head(10)

df.drop('min_salary',axis=1,inplace=True)

df.columns

df.head()

df.to_csv('data_cleaned.csv',index=0)

